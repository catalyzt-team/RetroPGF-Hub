      <div class="text-lg font-semibold my-4">Defining Impact = Profit</div>
      <div>
        One of the Optimism Collective's core values is "impact = profit," a
        principle that individuals should receive profit equivalent to the
        impact they contribute to the collective. However, applying this
        framework in practice can be challenging, particularly without a clear
        and quantitative definition of "impact" or "profit."
      </div>
      <ol class="list-disc ml-5 my-5">
        <li>
          <div class="font-medium">The Challenge of Interpretation</div>
          <div class="text-gray-600">
            Evaluating the types of public goods deserving funding is one of the
            most intricate aspects of Retroactive Public Goods Funding
            (RetroPGF). Badgeholders widely expressed the need for greater
            clarity in impact evaluation and philosophical alignment on the
            types of projects RetroPGF should support.
          </div>
          <div class="px-8 my-5 rounded-xl">
            <div class="italic text-[#ff1818]">
              "Are we taking impact vs. profit literally?‚Äù
            </div>
            <div class="italic text-[#ff1818]">
              ‚ÄúOn the philosophy of impact = profit, we wish there was a clearer
              definition of what impact looks like.‚Äù
            </div>
          </div>
        </li>
        <li>
          <div class="font-medium">Seeking Clarity in Evaluation</div>
          <div class="px-8 my-5 rounded-xl">
            <div class="italic text-[#ff1818]">
              ‚ÄúIntroduce better categories and stronger evaluation heuristics
              for each category.‚Äù
            </div>
            <div class="italic text-[#ff1818]">
              ‚ÄúMore discussion and structure of what the assessment criteria are
              - what are the goals and criteria that everyone can agree on.‚Äù
            </div>
            <div class="italic text-[#ff1818]">
              ‚ÄúDifferent people were assessing different ways and had their own
